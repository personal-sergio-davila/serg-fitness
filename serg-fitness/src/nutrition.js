export const UNIT_CONVERSIONS = {
  "oz": 28.35,
  "g": 1,
  "kg": 1000,
  "cup": 240,
  "tbsp": 15,
  "tsp": 5,
  "ml": 1,
  "lb": 453.6,
}

export const searchIngredient = (query) => {
  const q = query.toLowerCase().trim()
  const keys = Object.keys(NUTRITION_DB)
  const exact = keys.find(k => k === q)
  if (exact) return { key: exact, ...NUTRITION_DB[exact] }
  const partial = keys.filter(k => k.includes(q) || q.includes(k))
  if (partial.length > 0) return { key: partial[0], ...NUTRITION_DB[partial[0]] }
  return null
}

export const calculateMacros = (name, qty, unit) => {
  const found = searchIngredient(name)
  if (!found) return null
  const conv = UNIT_CONVERSIONS[unit?.toLowerCase()]
  if (!conv && found.unit) {
    const amount = parseFloat(qty) || 1
    return {
      cal: Math.round(found.cal * amount),
      protein: Math.round(found.protein * amount * 10) / 10,
      carbs: Math.round(found.carbs * amount * 10) / 10,
      fat: Math.round(found.fat * amount * 10) / 10,
      found: true
    }
  }
  if (!conv) return null
  const grams = (parseFloat(qty) || 0) * conv
  const ratio = grams / found.per
  return {
    cal: Math.round(found.cal * ratio),
    protein: Math.round(found.protein * ratio * 10) / 10,
    carbs: Math.round(found.carbs * ratio * 10) / 10,
    fat: Math.round(found.fat * ratio * 10) / 10,
    found: true
  }
}
